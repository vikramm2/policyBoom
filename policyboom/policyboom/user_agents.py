def get_random_user_agent() -> str:
    """
    Get a random user-agent string from the pool.
    
    Returns:
        str: A realistic browser user-agent string
    """
    return random.choice(USER_AGENTS)


def get_headers(additional_headers: dict = None) -> dict:
    """
    Get HTTP headers with a random user-agent and common browser headers.
    
    Args:
        additional_headers: Optional dict of additional headers to include
        
    Returns:
        dict: Complete set of HTTP headers
    """
    headers = {
        "User-Agent": get_random_user_agent(),
        "Accept": "text/html,application/xhtml+xml,application/xml;q=0.9,image/webp,*/*;q=0.8",
        "Accept-Language": "en-US,en;q=0.9",
        "Accept-Encoding": "gzip, deflate, br",
        "DNT": "1",
        "Connection": "keep-alive",
        "Upgrade-Insecure-Requests": "1",
    }
    
    if additional_headers:
        headers.update(additional_headers)
    
    return headers
